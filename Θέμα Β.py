# --- Συναρτήσεις ---

def eisagogi_vathmon():
    """
    Συνάρτηση που διαβάζει τους βαθμούς 15 φοιτητών
    και τους επιστρέφει σε μια λίστα.
    """
    lista_vathmon = [] # Δημιουργία κενής λίστας
    print("--- Εισαγωγή Βαθμολογιών (15 Φοιτητές) ---")
    
    # Επανάληψη για 15 φοιτητές (range 0 έως 14)
    for i in range(15):
        while True: # Έλεγχος εγκυρότητας για να μην κρασάρει αν δοθεί γράμμα
            try:
                # Ζητάμε τον βαθμό. Το i+1 είναι για να φαίνεται 1, 2... αντί για 0, 1...
                vathmos = float(input(f"Δώστε τον βαθμό του φοιτητή {i+1}: "))
                
                # Προαιρετικός έλεγχος αν ο βαθμός είναι λογικός (0-10)
                if 0 <= vathmos <= 10:
                    lista_vathmon.append(vathmos) # Προσθήκη στη λίστα
                    break # Έξοδος από το while αφού δόθηκε έγκυρος βαθμός
                else:
                    print("Παρακαλώ δώστε βαθμό από 0 έως 10.")
            except ValueError:
                print("Σφάλμα: Παρακαλώ δώστε αριθμό.")
                
    return lista_vathmon # Επιστροφή της λίστας στο κυρίως πρόγραμμα


def ypologismos_mesou_orou(lista):
    """
    Συνάρτηση που δέχεται μια λίστα βαθμών
    και επιστρέφει τον μέσο όρο.
    """
    synolo = sum(lista) # Αθροισμα όλων των στοιχείων της λίστας
    plithos = len(lista) # Πλήθος των στοιχείων της λίστας
    
    if plithos == 0:
        return 0
    
    mo = synolo / plithos
    return mo


def emfanisi_apotelesmatwn(lista, mo):
    """
    Συνάρτηση που δέχεται τη λίστα και τον Μ.Ο.
    Τυπώνει τον Μ.Ο. και τους βαθμούς που είναι μεγαλύτεροι από αυτόν.
    """
    print("-" * 30)
    print(f"Ο Μέσος Όρος της τάξης είναι: {mo:.2f}") # Στρογγυλοποίηση σε 2 δεκαδικά
    print("-" * 30)
    print("Οι βαθμοί πάνω από τον Μέσο Όρο είναι:")
    
    count = 0 # Μετρητής για το πόσοι πέρασαν τον Μ.Ο.
    
    # Επανάληψη για κάθε βαθμό μέσα στη λίστα
    for vathmos in lista:
        if vathmos > mo:
            print(f"- {vathmos}")
            count += 1
            
    if count == 0:
        print("Κανένας βαθμός δεν είναι πάνω από τον μέσο όρο.")


# --- Κυρίως Πρόγραμμα (Main) ---

# 1. Κλήση της συνάρτησης εισαγωγής
# Η μεταβλητή vathmologies παίρνει τη λίστα που επιστρέφει η συνάρτηση
vathmologies = eisagogi_vathmon()

# 2. Κλήση της συνάρτησης υπολογισμού μέσου όρου
# Περνάμε ως όρισμα τη λίστα που πήραμε πριν
mesos_oros = ypologismos_mesou_orou(vathmologies)

# 3. Κλήση της συνάρτησης εμφάνισης αποτελεσμάτων
# Περνάμε ως ορίσματα τη λίστα και τον μέσο όρο
emfanisi_apotelesmatwn(vathmologies, mesos_oros)